/** Allocator scope — tracks every _malloc so a single release frees them all. */
class AllocScope {
  private readonly ptrs: number[] = [];
  constructor(private readonly m: HTTPModule) {}

  alloc(size: number): number {
    const ptr = this.m._malloc(size);
    this.ptrs.push(ptr);
    return ptr;
  }

  allocString(str: string): number {
    const len = this.m.lengthBytesUTF8(str) + 1;
    const ptr = this.alloc(len);
    this.m.stringToUTF8(str, ptr, len);
    return ptr;
  }

  allocBytes(bytes: Uint8Array): number {
    const ptr = this.alloc(bytes.length);
    writeBytes(this.m, bytes, ptr);
    return ptr;
  }

  free(): void {
    for (const ptr of this.ptrs) {
      try { this.m._free(ptr); } catch { /* noop */ }
    }
    this.ptrs.length = 0;
  }
}

function writeBytes(m: HTTPModule, src: Uint8Array, destPtr: number): void {
  if (m.HEAPU8) {
    m.HEAPU8.set(src, destPtr);
    return;
  }
  for (let i = 0; i < src.length; i++) m.setValue(destPtr + i, src[i], 'i8');
}

function readBytes(m: HTTPModule, srcPtr: number, length: number): Uint8Array {
  if (srcPtr === 0 || length <= 0) return new Uint8Array(0);
  if (m.HEAPU8) return m.HEAPU8.slice(srcPtr, srcPtr + length);
  const out = new Uint8Array(length);
  for (let i = 0; i < length; i++) out[i] = m.getValue(srcPtr + i, 'i8') & 0xff;
  return out;
}

function required<T>(fn: T | undefined, name: string): T {
  if (fn === undefined || fn === null) {
    throw new Error(
      `HTTPAdapter: WASM module is missing export '${name}'. ` +
      `Rebuild the WASM target with the T3.13 HTTP exports enabled.`,
    );
  }
  return fn;
}

function writeHeaders(scope: AllocScope, m: HTTPModule, headers: HTTPHeader[] | undefined): { ptr: number; count: number } {
  if (!headers || headers.length === 0) return { ptr: 0, count: 0 };
  const kvSize = required(m._rac_wasm_sizeof_http_header_kv, 'rac_wasm_sizeof_http_header_kv')();
  const offName = required(m._rac_wasm_offsetof_http_header_kv_name, 'rac_wasm_offsetof_http_header_kv_name')();
  const offValue = required(m._rac_wasm_offsetof_http_header_kv_value, 'rac_wasm_offsetof_http_header_kv_value')();
  const arrayPtr = scope.alloc(kvSize * headers.length);
  for (let i = 0; i < headers.length; i++) {
    const itemPtr = arrayPtr + i * kvSize;
    m.setValue(itemPtr + offName, scope.allocString(headers[i].name), '*');
    m.setValue(itemPtr + offValue, scope.allocString(headers[i].value), '*');
  }
  return { ptr: arrayPtr, count: headers.length };
}

function writeRequestStruct(scope: AllocScope, m: HTTPModule, req: HTTPRequest): number {
  const size = required(m._rac_wasm_sizeof_http_request, 'rac_wasm_sizeof_http_request')();
  const ptr = scope.alloc(size);
  for (let i = 0; i < size; i++) m.setValue(ptr + i, 0, 'i8');

  const { ptr: headersPtr, count: headerCount } = writeHeaders(scope, m, req.headers);

  m.setValue(ptr + required(m._rac_wasm_offsetof_http_request_method, 'rac_wasm_offsetof_http_request_method')(),
    scope.allocString(req.method ?? 'GET'), '*');
  m.setValue(ptr + required(m._rac_wasm_offsetof_http_request_url, 'rac_wasm_offsetof_http_request_url')(),
    scope.allocString(req.url), '*');
  m.setValue(ptr + required(m._rac_wasm_offsetof_http_request_headers, 'rac_wasm_offsetof_http_request_headers')(),
    headersPtr, '*');
  m.setValue(ptr + required(m._rac_wasm_offsetof_http_request_header_count, 'rac_wasm_offsetof_http_request_header_count')(),
    headerCount, 'i32');

  if (req.body && req.body.length > 0) {
    m.setValue(ptr + required(m._rac_wasm_offsetof_http_request_body_bytes, 'rac_wasm_offsetof_http_request_body_bytes')(),
      scope.allocBytes(req.body), '*');
    m.setValue(ptr + required(m._rac_wasm_offsetof_http_request_body_len, 'rac_wasm_offsetof_http_request_body_len')(),
      req.body.length, 'i32');
  }

  m.setValue(ptr + required(m._rac_wasm_offsetof_http_request_timeout_ms, 'rac_wasm_offsetof_http_request_timeout_ms')(),
    req.timeoutMs ?? 0, 'i32');
  m.setValue(ptr + required(m._rac_wasm_offsetof_http_request_follow_redirects, 'rac_wasm_offsetof_http_request_follow_redirects')(),
    req.followRedirects === false ? RAC_FALSE : RAC_TRUE, 'i32');

  if (req.expectedChecksumHex) {
    m.setValue(ptr + required(m._rac_wasm_offsetof_http_request_expected_checksum_hex, 'rac_wasm_offsetof_http_request_expected_checksum_hex')(),
      scope.allocString(req.expectedChecksumHex), '*');
  }

  return ptr;
}

function readResponseStruct(m: HTTPModule, respPtr: number): HTTPResponse {
  const offStatus = required(m._rac_wasm_offsetof_http_response_status, 'rac_wasm_offsetof_http_response_status')();
  const offBody = required(m._rac_wasm_offsetof_http_response_body_bytes, 'rac_wasm_offsetof_http_response_body_bytes')();
  const offBodyLen = required(m._rac_wasm_offsetof_http_response_body_len, 'rac_wasm_offsetof_http_response_body_len')();
  const offRedirected = required(m._rac_wasm_offsetof_http_response_redirected_url, 'rac_wasm_offsetof_http_response_redirected_url')();

  const status = m.getValue(respPtr + offStatus, 'i32');
  const bodyPtr = m.getValue(respPtr + offBody, '*');
  const bodyLen = m.getValue(respPtr + offBodyLen, 'i32');
  const redirectedPtr = m.getValue(respPtr + offRedirected, '*');

  // Copy bytes out of WASM heap — the buffer is freed by
  // rac_http_response_free and would otherwise become a dangling view.
  const body = bodyPtr !== 0 && bodyLen > 0 ? readBytes(m, bodyPtr, bodyLen) : null;
  const redirectedUrl = redirectedPtr !== 0 ? m.UTF8ToString(redirectedPtr) : null;

  return { status, body, redirectedUrl };
}

