/**
 * HTTPAdapter.ts (Web / WASM) — T3.13.
 *
 * Thin TypeScript wrapper around the commons libcurl-backed C ABI
 * declared in:
 *   sdk/runanywhere-commons/include/rac/infrastructure/http/rac_http_client.h
 *   sdk/runanywhere-commons/include/rac/infrastructure/http/rac_http_download.h
 *
 * Why this exists:
 *   Before T3.13 every Web SDK site re-implemented HTTP with browser fetch
 *   + ad-hoc progress loops. Parity with the other SDKs (Swift /
 *   Kotlin / React Native / Flutter) now routes through the commons
 *   HTTP C ABI so redirects, resume, checksum, and progress semantics
 *   are identical across platforms.
 *
 * Module injection:
 *   The Web backend packages (`@runanywhere/web-llamacpp`,
 *   `@runanywhere/web-onnx`) each load an independent Emscripten
 *   module. They call `HTTPAdapter.setDefaultModule(module)` on load,
 *   so the core package (pure TypeScript) can reach into C++ without
 *   a hard dependency on any specific backend. Core callers that want
 *   to issue HTTP requests ask for `HTTPAdapter.tryDefault()`; when
 *   no backend has loaded yet, the helper returns `null` and the
 *   caller may fall back to browser fetch only for the carve-outs
 *   documented in `HTTP_FETCH_CARVE_OUTS`.
 *
 * Threading / blocking:
 *   `_rac_http_request_send` / `_rac_http_request_stream` /
 *   `_rac_http_download_execute` block the calling thread inside
 *   libcurl. Emscripten serves network I/O through the browser's
 *   fetch/XHR under the hood, so on the main thread these calls
 *   must be run via `ccall({ async: true })` (ASYNCIFY / JSPI). The
 *   adapter always uses `{ async: true }` so it works from either a
 *   dedicated worker or the main thread without caller-side changes.
 */

import { SDKLogger } from '../Foundation/SDKLogger';
import { FetchHttpTransport } from './FetchHttpTransport';

const logger = new SDKLogger('HTTPAdapter');

/**
 * The only legitimate direct browser-fetch carve-outs in the Web SDK.
 * Everything else should route through `HTTPAdapter` once a backend
 * Emscripten module with `rac_http_*` exports is available.
 */
export const HTTP_FETCH_CARVE_OUTS = {
  bootstrapOnly: 'bootstrap-only: fetches the WASM/glue asset needed before any HTTPAdapter-capable module exists',
  browserOnlyBlobImport: 'browser-only blob import: fetches JavaScript text that must be patched and imported from a Blob URL',
  noWasmModuleRegisteredFallback: 'fallback when no WASM module registered: preserves pure-core or ONNX-only browser usage before an HTTPAdapter default exists',
} as const;

// ---------------------------------------------------------------------------
// Module shape — the minimum Emscripten surface HTTPAdapter needs.
// Re-declared locally so core stays decoupled from any one backend's
// module typings (`LlamaCppModule`, `SherpaONNXModule`, ...).
// ---------------------------------------------------------------------------

export interface HTTPModule {
  _malloc(size: number): number;
  _free(ptr: number): void;
  setValue(ptr: number, value: number, type: string): void;
  getValue(ptr: number, type: string): number;
  UTF8ToString(ptr: number, maxBytesToRead?: number): string;
  stringToUTF8(str: string, ptr: number, maxBytesToWrite: number): void;
  lengthBytesUTF8(str: string): number;

  addFunction(fn: (...args: number[]) => number | void, signature: string): number;
  removeFunction(ptr: number): void;

  HEAPU8?: Uint8Array;

  ccall: (
    name: string,
    returnType: string | null,
    argTypes: string[],
    args: unknown[],
    opts?: { async?: boolean },
  ) => unknown;

  // HTTP exports — all optional at type level so failure surfaces early
  // with a clear error ("module does not export rac_http_*") rather than
  // a TypeError on undefined().
  _rac_http_client_create?(outPtr: number): number;
  _rac_http_client_destroy?(clientPtr: number): void;
  _rac_http_response_free?(respPtr: number): void;

  // Transport registry (v2 close-out Phase H2 / H7). Present only on
  // the WASM build of commons — registers the emscripten_fetch-backed
  // adapter with the platform HTTP router so the libcurl fallback
  // (compiled out on WASM) is bypassed for every `rac_http_request_*`
  // call. Returns `RAC_SUCCESS` (0) on success.
  _rac_http_transport_register_emscripten?(): number;
  _rac_http_transport_is_registered?(): number;

  // Stage 3d — JS-side transport registration. The JS layer installs
  // function-table indices for the transport vtable directly (see
  // FetchHttpTransport.ts) so requests can be routed through `fetch()`
  // without bouncing through `emscripten_fetch`. Pass 0 for any op you
  // want to fall back on the emscripten_fetch path; all-zero unregisters.
  _rac_http_transport_register_from_js?(
    requestSendPtr: number,
    requestStreamPtr: number,
    requestResumePtr: number,
  ): number;

  // Struct layout helpers — generated by wasm_exports.cpp.
  _rac_wasm_sizeof_http_request?(): number;
  _rac_wasm_sizeof_http_response?(): number;
  _rac_wasm_sizeof_http_download_request?(): number;
  _rac_wasm_sizeof_http_header_kv?(): number;
  _rac_wasm_offsetof_http_request_method?(): number;
  _rac_wasm_offsetof_http_request_url?(): number;
  _rac_wasm_offsetof_http_request_headers?(): number;
  _rac_wasm_offsetof_http_request_header_count?(): number;
  _rac_wasm_offsetof_http_request_body_bytes?(): number;
  _rac_wasm_offsetof_http_request_body_len?(): number;
  _rac_wasm_offsetof_http_request_timeout_ms?(): number;
  _rac_wasm_offsetof_http_request_follow_redirects?(): number;
  _rac_wasm_offsetof_http_request_expected_checksum_hex?(): number;
  _rac_wasm_offsetof_http_response_status?(): number;
  _rac_wasm_offsetof_http_response_headers?(): number;
  _rac_wasm_offsetof_http_response_header_count?(): number;
  _rac_wasm_offsetof_http_response_body_bytes?(): number;
  _rac_wasm_offsetof_http_response_body_len?(): number;
  _rac_wasm_offsetof_http_response_redirected_url?(): number;
  _rac_wasm_offsetof_http_header_kv_name?(): number;
  _rac_wasm_offsetof_http_header_kv_value?(): number;
  _rac_wasm_offsetof_http_download_request_url?(): number;
  _rac_wasm_offsetof_http_download_request_destination_path?(): number;
  _rac_wasm_offsetof_http_download_request_headers?(): number;
  _rac_wasm_offsetof_http_download_request_header_count?(): number;
  _rac_wasm_offsetof_http_download_request_timeout_ms?(): number;
  _rac_wasm_offsetof_http_download_request_follow_redirects?(): number;
  _rac_wasm_offsetof_http_download_request_resume_from_byte?(): number;
  _rac_wasm_offsetof_http_download_request_expected_sha256_hex?(): number;

  // Optional FS handle — used by `download()` since the C ABI writes to
  // a filesystem path and callers usually want bytes back. Not all
  // backend modules configure the same FS shape, hence the loose type.
  FS?: {
    readFile(path: string): Uint8Array;
    unlink(path: string): void;
    writeFile?(path: string, data: Uint8Array): void;
    mkdir?(path: string): void;
    analyzePath?(path: string): { exists: boolean };
  };
  FS_createPath?(parent: string, path: string, canRead: boolean, canWrite: boolean): void;
}

// ---------------------------------------------------------------------------
// Public request/response value types
// ---------------------------------------------------------------------------

export interface HTTPHeader {
  name: string;
  value: string;
}

/** JS-side descriptor for a blocking or streaming HTTP request. */
export interface HTTPRequest {
  method?: string;                 // default: 'GET'
  url: string;
  headers?: HTTPHeader[];
  body?: Uint8Array;
  timeoutMs?: number;              // default: 0 (no timeout)
  followRedirects?: boolean;       // default: true
  expectedChecksumHex?: string;    // advisory only, HTTP client does not verify
}

/** Response from a blocking send. Body is null for streaming calls. */
export interface HTTPResponse {
  status: number;
  body: Uint8Array | null;
  redirectedUrl: string | null;
}

/** Body chunk callback for streaming requests. Return false to cancel. */
export type ChunkHandler = (chunk: Uint8Array, totalWritten: number, contentLength: number) => boolean | void;

/** Progress callback for downloads (`rac_http_download_execute`). */
export type DownloadProgressHandler = (bytesWritten: number, totalBytes: number) => boolean | void;

export interface DownloadRequest {
  url: string;
  destinationPath: string;
  headers?: HTTPHeader[];
  timeoutMs?: number;
  followRedirects?: boolean;
  resumeFromByte?: number;
  expectedSha256Hex?: string;
}

// Mirrors rac_http_download_status_t in rac_http_download.h — keep in sync.
export enum DownloadStatus {
  OK = 0,
  NetworkError = 1,
  FileError = 2,
  InsufficientStorage = 3,
  InvalidUrl = 4,
  ChecksumFailed = 5,
  Cancelled = 6,
  ServerError = 7,
  Timeout = 8,
  NetworkUnavailable = 9,
  DnsError = 10,
  SslError = 11,
  Unknown = 99,
}

// RAC_TRUE / RAC_FALSE from rac_types.h — curl-backed client returns
// these raw ints through the chunk / progress callbacks.
const RAC_TRUE = 1;
const RAC_FALSE = 0;

