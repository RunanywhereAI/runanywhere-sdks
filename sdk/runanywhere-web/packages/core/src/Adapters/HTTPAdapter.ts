/**
 * Thin OO wrapper over `rac_http_client_*` / `rac_http_download_execute`.
 * Each instance binds to a single Emscripten module. Stateless beyond
 * that — a fresh C client handle is created/destroyed per call so
 * requests can safely be issued concurrently from JS (libcurl easy
 * handles are not thread-safe, so sharing one across overlapping
 * awaits would be incorrect).
 */
export class HTTPAdapter {
  constructor(private readonly m: HTTPModule) {}

  /** Blocking request — buffers the full body into memory. */
  async request(req: HTTPRequest): Promise<HTTPResponse> {
    const clientPtr = await this.createClient();
    const scope = new AllocScope(this.m);
    const respSize = required(this.m._rac_wasm_sizeof_http_response, 'rac_wasm_sizeof_http_response')();
    const respPtr = scope.alloc(respSize);
    for (let i = 0; i < respSize; i++) this.m.setValue(respPtr + i, 0, 'i8');

    try {
      const reqPtr = writeRequestStruct(scope, this.m, req);
      const rc = await this.ccall('rac_http_request_send', 'number', ['number', 'number', 'number'], [clientPtr, reqPtr, respPtr]);
      if (rc !== 0) {
        throw new Error(`rac_http_request_send failed: rc=${rc} url=${req.url}`);
      }
      const response = readResponseStruct(this.m, respPtr);
      required(this.m._rac_http_response_free, 'rac_http_response_free')(respPtr);
      return response;
    } finally {
      scope.free();
      this.destroyClient(clientPtr);
    }
  }

  /**
   * Streaming request — body chunks are delivered via `onChunk`. The
   * body is NEVER buffered in memory by the adapter, so this is the
   * right call for model-sized payloads.
   *
   * The chunk handler can return `false` to abort the transfer; the
   * underlying curl connection is torn down immediately and the
   * returned promise rejects with a cancellation error.
   */
  async stream(req: HTTPRequest, onChunk: ChunkHandler): Promise<HTTPResponse> {
    const clientPtr = await this.createClient();
    const scope = new AllocScope(this.m);
    const respSize = required(this.m._rac_wasm_sizeof_http_response, 'rac_wasm_sizeof_http_response')();
    const respPtr = scope.alloc(respSize);
    for (let i = 0; i < respSize; i++) this.m.setValue(respPtr + i, 0, 'i8');

    // Function-table trampoline for the C body-chunk callback. Must
    // match rac_http_body_chunk_fn: rac_bool_t (const uint8_t*, size_t,
    // uint64_t total_written, uint64_t content_length, void* user_data)
    // → signature 'iiijji' (i=int, j=int64).
    let cancelled = false;
    const cbPtr = this.m.addFunction(
      (chunkPtr: number, chunkLen: number, totalWritten: number, _totalWrittenHi: number, contentLength: number /* and hi + user_data further args */) => {
        try {
          const bytes = readBytes(this.m, chunkPtr, chunkLen);
          const keep = onChunk(bytes, totalWritten, contentLength);
          if (keep === false) {
            cancelled = true;
            return RAC_FALSE;
          }
          return RAC_TRUE;
        } catch (err) {
          cancelled = true;
          logger.warning(`HTTPAdapter.stream chunk handler threw: ${err instanceof Error ? err.message : String(err)}`);
          return RAC_FALSE;
        }
      },
      'iiiijji',
    );

    try {
      const reqPtr = writeRequestStruct(scope, this.m, req);
      const rc = await this.ccall('rac_http_request_stream', 'number',
        ['number', 'number', 'number', 'number', 'number'],
        [clientPtr, reqPtr, cbPtr, 0, respPtr]);
      if (rc !== 0) {
        const reason = cancelled ? 'cancelled by chunk handler' : `rc=${rc}`;
        throw new Error(`rac_http_request_stream failed: ${reason} url=${req.url}`);
      }
      const response = readResponseStruct(this.m, respPtr);
      required(this.m._rac_http_response_free, 'rac_http_response_free')(respPtr);
      return response;
    } finally {
      this.m.removeFunction(cbPtr);
      scope.free();
      this.destroyClient(clientPtr);
    }
  }

  /**
   * File download via `rac_http_download_execute`. Writes bytes to the
   * Emscripten virtual filesystem at `destinationPath`. The optional
   * progress callback fires on every chunk libcurl delivers.
   *
   * Callers that want the bytes back in JS should read the file from
   * the module's FS after the promise resolves. This matches the
   * native behaviour (iOS / Android / RN all hand the caller a file
   * path, not a buffer).
   */
  async download(req: DownloadRequest, onProgress?: DownloadProgressHandler): Promise<DownloadStatus> {
    const scope = new AllocScope(this.m);

    // Function-table trampoline for rac_http_download_progress_fn:
    //   rac_bool_t (uint64_t bytes_written, uint64_t total_bytes,
    //               void* user_data)
    // → signature 'ijji' (i=return int, j=int64, i=int).
    let cancelled = false;
    const progressPtr = onProgress
      ? this.m.addFunction(
          (bytesWritten: number, _bytesWrittenHi: number, totalBytes: number /* and hi + user_data */) => {
            try {
              const keep = onProgress(bytesWritten, totalBytes);
              if (keep === false) {
                cancelled = true;
                return RAC_FALSE;
              }
              return RAC_TRUE;
            } catch (err) {
              cancelled = true;
              logger.warning(`HTTPAdapter.download progress handler threw: ${err instanceof Error ? err.message : String(err)}`);
              return RAC_FALSE;
            }
          },
          'ijji',
        )
      : 0;

    const outHttpStatusPtr = scope.alloc(4);
    this.m.setValue(outHttpStatusPtr, 0, 'i32');

    try {
      const reqPtr = writeDownloadRequestStruct(scope, this.m, req);
      const status = await this.ccall('rac_http_download_execute', 'number',
        ['number', 'number', 'number', 'number'],
        [reqPtr, progressPtr, 0, outHttpStatusPtr]) as number;
      if (status !== DownloadStatus.OK && cancelled) {
        return DownloadStatus.Cancelled;
      }
      return status as DownloadStatus;
    } finally {
      if (progressPtr !== 0) this.m.removeFunction(progressPtr);
      scope.free();
    }
  }

  // -------------------------------------------------------------------------
  // Helpers
  // -------------------------------------------------------------------------

  private async createClient(): Promise<number> {
    const outPtrPtr = this.m._malloc(4);
    try {
      this.m.setValue(outPtrPtr, 0, '*');
      const rc = await this.ccall('rac_http_client_create', 'number', ['number'], [outPtrPtr]) as number;
      if (rc !== 0) throw new Error(`rac_http_client_create failed: rc=${rc}`);
      const clientPtr = this.m.getValue(outPtrPtr, '*');
      if (clientPtr === 0) throw new Error('rac_http_client_create returned NULL');
      return clientPtr;
    } finally {
      this.m._free(outPtrPtr);
    }
  }

  private destroyClient(clientPtr: number): void {
    if (clientPtr === 0) return;
    try {
      required(this.m._rac_http_client_destroy, 'rac_http_client_destroy')(clientPtr);
    } catch (err) {
      logger.warning(`rac_http_client_destroy threw: ${err instanceof Error ? err.message : String(err)}`);
    }
  }

  private async ccall(name: string, returnType: string | null, argTypes: string[], args: unknown[]): Promise<number> {
    // `async: true` wraps the invocation in ASYNCIFY/JSPI so libcurl
    // can suspend inside Emscripten's fetch-backed socket layer without
    // blocking the JS event loop.
    const result = this.m.ccall(name, returnType, argTypes, args, { async: true });
    if (result && typeof (result as { then?: unknown }).then === 'function') {
      return (await (result as Promise<number>));
    }
    return result as number;
  }

  // =========================================================================
  // Default-module registry
  //
  // Backend packages (`@runanywhere/web-llamacpp`, `@runanywhere/web-onnx`)
  // install their Emscripten module here after WASM load. Core callers
  // that don't have direct access to a backend module use
  // `HTTPAdapter.tryDefault()` to reach the first one that registered.
  // =========================================================================

  private static defaultInstance: HTTPAdapter | null = null;

  static setDefaultModule(module: HTTPModule): void {
    HTTPAdapter.defaultInstance = new HTTPAdapter(module);

    // v2 close-out Phase H7: on the WASM commons build the
    // emscripten_fetch-backed adapter registers itself via the platform
    // HTTP transport vtable so the router in rac_http_client_curl.cpp
    // short-circuits to it. The symbol is only exported from the WASM
    // build (see sdk/runanywhere-web/wasm/CMakeLists.txt
    // RAC_EXPORTED_FUNCTIONS list); on any other module it's missing and
    // we skip registration silently (the curl fallback handles requests).
    const register = module._rac_http_transport_register_emscripten;
    if (typeof register === 'function') {
      try {
        const rc = register();
        if (rc !== 0) {
          logger.warning(`rac_http_transport_register_emscripten returned rc=${rc}`);
        } else {
          logger.debug('Emscripten fetch HTTP transport registered via vtable');
        }
      } catch (err) {
        logger.warning(
          `rac_http_transport_register_emscripten threw: ${err instanceof Error ? err.message : String(err)}`,
        );
      }
    }

    // Stage 3d — JS-side adapter registration. Installed AFTER the
    // emscripten_fetch registration above so any subsequent HTTP request
    // goes through the JS trampolines when the module supports them. When
    // the build lacks the Stage 3d export (`_rac_http_transport_register_from_js`)
    // the install() call is a silent no-op and the emscripten_fetch
    // adapter above remains active.
    //
    // No circular import: FetchHttpTransport only imports `HTTPModule`
    // as a type from this file, so the TS side erases it at compile time.
    try {
      FetchHttpTransport.install(module);
    } catch (err) {
      logger.warning(
        `FetchHttpTransport.install threw: ${err instanceof Error ? err.message : String(err)}`,
      );
    }
  }

  static clearDefaultModule(): void {
    HTTPAdapter.defaultInstance = null;
  }

  static tryDefault(): HTTPAdapter | null {
    return HTTPAdapter.defaultInstance;
  }
}

function writeDownloadRequestStruct(scope: AllocScope, m: HTTPModule, req: DownloadRequest): number {
  const size = required(m._rac_wasm_sizeof_http_download_request, 'rac_wasm_sizeof_http_download_request')();
  const ptr = scope.alloc(size);
  for (let i = 0; i < size; i++) m.setValue(ptr + i, 0, 'i8');

  const { ptr: headersPtr, count: headerCount } = writeHeaders(scope, m, req.headers);

  m.setValue(ptr + required(m._rac_wasm_offsetof_http_download_request_url, 'rac_wasm_offsetof_http_download_request_url')(),
    scope.allocString(req.url), '*');
  m.setValue(ptr + required(m._rac_wasm_offsetof_http_download_request_destination_path, 'rac_wasm_offsetof_http_download_request_destination_path')(),
    scope.allocString(req.destinationPath), '*');
  m.setValue(ptr + required(m._rac_wasm_offsetof_http_download_request_headers, 'rac_wasm_offsetof_http_download_request_headers')(),
    headersPtr, '*');
  m.setValue(ptr + required(m._rac_wasm_offsetof_http_download_request_header_count, 'rac_wasm_offsetof_http_download_request_header_count')(),
    headerCount, 'i32');
  m.setValue(ptr + required(m._rac_wasm_offsetof_http_download_request_timeout_ms, 'rac_wasm_offsetof_http_download_request_timeout_ms')(),
    req.timeoutMs ?? 0, 'i32');
  m.setValue(ptr + required(m._rac_wasm_offsetof_http_download_request_follow_redirects, 'rac_wasm_offsetof_http_download_request_follow_redirects')(),
    req.followRedirects === false ? RAC_FALSE : RAC_TRUE, 'i32');
  // resume_from_byte is uint64_t — write low 32 bits, zero high half.
  // JS numbers beyond 2^32 aren't supported here; callers that need
  // multi-TB resume would hit Number.MAX_SAFE_INTEGER long before.
  const offResume = required(m._rac_wasm_offsetof_http_download_request_resume_from_byte, 'rac_wasm_offsetof_http_download_request_resume_from_byte')();
  m.setValue(ptr + offResume, (req.resumeFromByte ?? 0) & 0xffffffff, 'i32');
  m.setValue(ptr + offResume + 4, 0, 'i32');

  if (req.expectedSha256Hex) {
    m.setValue(ptr + required(m._rac_wasm_offsetof_http_download_request_expected_sha256_hex, 'rac_wasm_offsetof_http_download_request_expected_sha256_hex')(),
      scope.allocString(req.expectedSha256Hex), '*');
  }

  return ptr;
}
