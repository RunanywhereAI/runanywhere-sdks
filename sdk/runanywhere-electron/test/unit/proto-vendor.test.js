const { test } = require('node:test');
const assert = require('node:assert/strict');
const fs = require('fs');
const path = require('path');

// Drift guard: src/proto/rag.ts is VENDORED (copied) from the shared
// @runanywhere/proto-ts package because @runanywhere/electron is standalone and
// can't consume the workspace package. If idl/rag.proto evolves, the shared
// source is regenerated but the vendored copy would silently diverge. This test
// fails loudly on drift so the copy is kept in sync (re-copy the shared file).
const VENDOR = path.join(__dirname, '..', '..', 'src', 'proto', 'rag.ts');
const SHARED = path.join(__dirname, '..', '..', '..', 'shared', 'proto-ts', 'src', 'rag.ts');
const MARKER = '// Code generated by protoc-gen-ts_proto';

// The shared package lives elsewhere in the monorepo; skip when this is a
// partial checkout (e.g. the SDK extracted on its own).
const SHARED_PRESENT = fs.existsSync(SHARED);

/** Generated body after the vendored header (line-ending + trailing-ws normalized). */
function generatedBody(file) {
  const src = fs.readFileSync(file, 'utf8').replace(/\r\n/g, '\n');
  const i = src.indexOf(MARKER);
  assert.ok(i >= 0, `${file} is missing the protoc-gen-ts_proto marker`);
  return src.slice(i).trimEnd();
}

test('vendored src/proto/rag.ts matches the shared proto-ts source (no drift)', { skip: !SHARED_PRESENT }, () => {
  assert.equal(
    generatedBody(VENDOR),
    generatedBody(SHARED),
    'src/proto/rag.ts has drifted from sdk/shared/proto-ts/src/rag.ts — re-copy the shared file (keep only the vendored header).'
  );
});

test('the vendored file carries a provenance header explaining why it is copied', () => {
  const head = fs.readFileSync(VENDOR, 'utf8').slice(0, 600);
  assert.match(head, /VENDORED from @runanywhere\/proto-ts/);
});
